class MrCoxallStack {
  private stack: number[] = []

  getStack(): number[] {
    return this.stack
  }

  pushItem(pushedNumber: number): void {
    this.stack.push(pushedNumber)
  }

  popItem(): number | undefined {
    return this.stack.pop()
  }
}

export = MrCoxallStack
